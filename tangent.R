#---------------------------------------
# 1. Tangent
#---------------------------------------
th.lo = -4*pi
th.hi = 4 * pi
theta = th.lo + (th.hi - th.lo)*(0:1000)/1000

y=tan(theta)

#---------------------------------------
# 2. Draw the pic
#--------------------------------------- 
plot(theta, y , type="l", ylim=c(-2,2), xlab="theta", ylab="tangent")
